    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String WId=widField.getText();
        String newWordName=wordField.getText();
        String newMeaning=meaningField.getText();
        String newTopics=topicsField.getText();
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/technicalshow","root","Aman.komal2122");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("Select *from word where WId='"+WId+"'");
            if(rs.next())
            {
                 st.executeUpdate("update word set WordName='"+newWordName+"' where WId='"+WId+"'");
                 st.executeUpdate("update word set Meaning='"+newMeaning+"' where WId='"+WId+"'");
                 st.executeUpdate("update word set Topics='"+newTopics+"' where WId='"+WId+"'");
                 JOptionPane.showMessageDialog(null,"Word successfully Updated!!");
                 setVisible(false);
                 new wordOperations().setVisible(true);
            }
             else
                 JOptionPane.showMessageDialog(null,"Enter values properly!!");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Connection Error!!!");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

