    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String WId=widField.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection=DriverManager.getConnection("jdbc:mysql://localhost:3306/technicalshow","root","Aman.komal2122");
            Statement st=connection.createStatement();
            ResultSet rs=st.executeQuery("select *from word where WId='"+WId+"'");
            if(rs.next())
            {
                wordField.setText(rs.getString(2));
                meaningField.setText(rs.getString(3));
                topicsField.setText(rs.getString(4));
                
            }
            else if(widField.getText().length()<=0)
            {
                JOptionPane.showMessageDialog(null,"Enter some value in Id Box!!");
            }
            else
            {
                JFrame jf=new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf,"Word Id does not exist");
            }
        }
        catch(Exception e)
        {
            JFrame jf=new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf,e);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

